    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
    try  {
        Class.forName("com.mysql.cj.jdbc.Driver");
        con=DriverManager.getConnection( "jdbc:mysql://localhost:3306/bib","root","");
        String id = ID.getText();
        String nom1 = nom.getText();
        String sqlSelect = "SELECT * FROM abonne WHERE idab = ?";
        pst = con.prepareStatement(sqlSelect);
        pst.setString(1, id);
        ResultSet rs = pst.executeQuery();
        if (rs.next()) {
            JOptionPane.showMessageDialog(null, "Cette " + id + " existe déjà dans la base de données.");
        } else {
            String sqlInsert = "INSERT INTO abonne (idab, nomab) values (?, ?)";
            pst = con.prepareStatement(sqlInsert);
            pst.setString(1, id);
            pst.setString(2, nom1);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "emprunteur ajouté avec succès !");
        }
        con.close();
    } catch (Exception e) {
        e.printStackTrace();
    }
    JOptionPane.showMessageDialog(null, "L'ID de livre  existe déjà dans la base de données.");
    
}
